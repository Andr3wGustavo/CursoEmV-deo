# Estrutura de repetição For

for c in range(0, 6,):  # Basicamente ele vai escrever 'Oi' no terminal em 3 vezes
    print('Oi')
print('FiM')

for c1 in range(0, 6, -1):  # Ele conta até do 1 até 10 de regresivamente
    print(c1)
print('End')

for c2 in range (0, 7 , 2):  # Ele conta de 0 a 6, pulando de 2 em 2
    print(c2)
